import os
from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH

PTH = r"C:\Users\Danila\OneDrive\Desktop\agent\coursework_conclusion.docx"
doc = Document()

for s in doc.sections:
    s.top_margin = Cm(2)
    s.bottom_margin = Cm(2)
    s.left_margin = Cm(2.5)
    s.right_margin = Cm(1.5)

st = doc.styles["Normal"]
st.font.name = "Times New Roman"
st.font.size = Pt(14)
st.paragraph_format.line_spacing = 1.5
st.paragraph_format.space_after = Pt(0)
st.paragraph_format.space_before = Pt(0)

def H(t):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(t)
    r.bold = True
    r.font.name = "Times New Roman"
    r.font.size = Pt(16)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(12)
    p.paragraph_format.line_spacing = 1.5

def P(t, ind=True):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.line_spacing = 1.5
    pf.space_after = Pt(3)
    pf.space_before = Pt(0)
    if ind:
        pf.first_line_indent = Cm(1.25)
    r = p.add_run(t)
    r.font.name = "Times New Roman"
    r.font.size = Pt(14)

H("ЗАКЛЮЧЕНИЕ")

P("В ходе выполнения данной курсовой работы была достигнута поставленная цель: разработан прототип интеллектуального интерфейса для генерации SQL-запросов из запросов на естественном языке (NL2SQL), ориентированный на русскоязычных пользователей, не владеющих SQL. В процессе работы были решены все поставленные задачи, что позволяет сделать следующие выводы.")

P("В первой главе был проведён всесторонний анализ теоретических основ задачи NL2SQL. Рассмотрена формальная постановка задачи преобразования вопроса на естественном языке и схемы базы данных в SQL-запрос. Проанализирована эволюция подходов к решению данной задачи от правиловых систем 1970-х годов (LUNAR, CHAT-80) до современных методов на основе больших языковых моделей. Изучены ключевые архитектуры нейронных сетей, применяемые в NL2SQL: Transformer, BERT, RAT-SQL, BRIDGE, LGESQL, T5 и DAIL-SQL. Рассмотрены основные бенчмарки (Spider, WikiSQL, Bird) и метрики оценки (Execution Accuracy, Exact Set Match). Установлено, что современные LLM-подходы обеспечивают наилучшее качество генерации SQL, однако задача обработки русскоязычных запросов остаётся недостаточно исследованной.")

P("Во второй главе проведён анализ существующих NL2SQL-систем и подходов к их реализации. Рассмотрены и охарактеризованы шесть современных систем: Vanna AI (RAG-фреймворк), SQLCoder (специализированная модель), DAIL-SQL (маскирование схемы), DIN-SQL (декомпозиция задач), MAC-SQL (мультиагентная архитектура) и Chat2DB (прикладное решение). Выполнено сравнение пяти основных подходов к генерации SQL: zero-shot, few-shot, fine-tuning, RAG и агентный подход. На основе сравнительного анализа выбран комбинированный подход, сочетающий few-shot промптинг с селекцией схемы, как обеспечивающий наилучший баланс между качеством генерации и сложностью реализации. Определён технологический стек прототипа: Python, OpenAI API, SQLite, Streamlit, LangChain. Сформулированы функциональные и нефункциональные требования к системе.")

P("В третьей главе разработан и реализован прототип NL2SQL-системы, состоящий из шести модулей: предобработки естественного языка, селекции схемы базы данных, формирования промпта, генерации SQL, валидации и пользовательского интерфейса. Спроектирована и реализована тестовая база данных из четырёх таблиц (employees, projects, tasks, comments), охватывающая широкий спектр типов SQL-запросов. Разработан модуль обработки естественного языка с функциями очистки, определения языка, нормализации и выделения ключевых сущностей. Реализован модуль генерации SQL на основе OpenAI API с few-shot промптингом и селекцией схемы. Разработан модуль валидации с двухуровневой проверкой (синтаксическая и семантическая) и механизмом повторной генерации при ошибках. Создан пользовательский интерфейс на Streamlit, обеспечивающий удобное взаимодействие с системой.")

P("Тестирование прототипа на наборе из 30 запросов различной сложности показало следующие результаты: точность Execution Accuracy (EX) на простых запросах составила 86.5 процента, на запросах средней сложности 75 процентов, на сложных 55 процентов. Средняя точность на всём наборе 72 процента, что превышает целевой показатель в 70 процентов. Сравнение с базовыми подходами показало, что разработанный прототип обеспечивает на 50 процентов более высокую точность по сравнению с простым запросом к LLM без дополнительной обработки. Анализ ошибок выявил основные направления для дальнейшего улучшения системы.")

P("Практическая значимость разработанного прототипа заключается в создании инструмента, позволяющего пользователям, не владеющим SQL, получать доступ к данным в реляционных базах данных с помощью запросов на естественном языке. Система может быть использована в малых и средних организациях для упрощения доступа к корпоративным данным, а также в образовательных целях для изучения SQL. Благодаря модульной архитектуре прототип может быть легко адаптирован под конкретные предметные области и базы данных.")

P("Перспективы дальнейшего развития системы включают: расширение базы few-shot примеров для повышения точности генерации; поддержку специализированных моделей (SQLCoder) для улучшения качества на сложных запросах; внедрение мультиагентной архитектуры для декомпозиции сложных запросов; реализацию асинхронной обработки для повышения производительности; поддержку большего количества типов SQL-запросов (INSERT, UPDATE); интеграцию с различными СУБД помимо SQLite; разработку API для программного доступа к функциям системы. Данные направления развития позволят превратить прототип в полноценное промышленное решение.")

P("Таким образом, все задачи, поставленные во введении, выполнены в полном объёме: проанализированы теоретические основы NL2SQL, изучены существующие системы и подходы, разработан и протестирован прототип NL2SQL-системы, подтвердивший свою эффективность. Разработанный прототип может служить основой для дальнейших исследований и практических разработок в области Text-to-SQL.")


