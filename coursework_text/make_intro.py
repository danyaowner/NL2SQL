import os
from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH

PTH = r"C:\Users\Danila\OneDrive\Desktop\agent\coursework_intro.docx"
doc = Document()

for s in doc.sections:
    s.top_margin = Cm(2)
    s.bottom_margin = Cm(2)
    s.left_margin = Cm(2.5)
    s.right_margin = Cm(1.5)

st = doc.styles["Normal"]
st.font.name = "Times New Roman"
st.font.size = Pt(14)
st.paragraph_format.line_spacing = 1.5
st.paragraph_format.space_after = Pt(0)
st.paragraph_format.space_before = Pt(0)

def H(t):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(t)
    r.bold = True
    r.font.name = "Times New Roman"
    r.font.size = Pt(16)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(12)
    p.paragraph_format.line_spacing = 1.5

def P(t, ind=True):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.line_spacing = 1.5
    pf.space_after = Pt(3)
    pf.space_before = Pt(0)
    if ind: pf.first_line_indent = Cm(1.25)
    r = p.add_run(t)
    r.font.name = "Times New Roman"
    r.font.size = Pt(14)

H("ВВЕДЕНИЕ")

P("В последние десятилетия объём данных, генерируемых и накапливаемых организациями, растёт экспоненциально. Большинство этих данных хранится в реляционных базах данных, доступ к которым осуществляется с помощью языка структурированных запросов — SQL. SQL является мощным и гибким инструментом, однако его эффективное освоение требует понимания реляционной алгебры, знания синтаксиса и опыта работы с конкретными СУБД. Это создаёт серьёзный барьер для специалистов, которые не имеют профильной технической подготовки, но регулярно нуждаются в извлечении данных из баз для выполнения своих рабочих задач — аналитиков, менеджеров, маркетологов и исследователей.")

P("Проблема преобразования запросов с естественного языка в формальные SQL-запросы (Natural Language to SQL, NL2SQL) привлекает внимание исследователей на протяжении нескольких десятилетий. Первые попытки решить эту задачу предпринимались ещё в 1970-х годах в рамках систем вопросно-ответного типа, таких как LUNAR и CHAT-80. Однако долгое время практическое применение NL2SQL-систем оставалось ограниченным из-за недостаточной точности и узости предметных областей, в которых они могли работать.")

P("Ситуация кардинально изменилась с появлением глубоких нейронных сетей и, в особенности, архитектуры Transformer, предложенной Vaswani и соавторами в 2017 году. Эта архитектура легла в основу больших языковых моделей (Large Language Models, LLM), которые продемонстрировали впечатляющие результаты в задачах понимания и генерации текста. Такие модели, как GPT-4, DeepSeek и Claude, способны не только интерпретировать естественный язык на высоком уровне, но и генерировать корректный код, включая SQL-запросы различной сложности.")

P("Актуальность темы данной курсовой работы обусловлена несколькими факторами. Во-первых, несмотря на значительный прогресс в области NL2SQL, существующие промышленные решения либо являются проприетарными и дорогостоящими, либо требуют глубоких технических знаний для настройки и развёртывания. Во-вторых, большинство современных NL2SQL-систем ориентированы на английский язык, тогда как для русскоязычных пользователей качество генерации SQL-запросов остаётся недостаточно изученным. В-третьих, стремительное развитие LLM открывает новые возможности для создания интуитивно понятных интерфейсов взаимодействия с базами данных, однако требует пересмотра традиционных подходов к проектированию таких систем.")

P("Объектом исследования данной работы являются методы и алгоритмы преобразования запросов на естественном языке в SQL-запросы, включая подходы на основе больших языковых моделей и нейросетевых архитектур.")

P("Предметом исследования выступает процесс разработки прототипа интеллектуального интерфейса для генерации SQL-запросов, включающий проектирование архитектуры программного решения, выбор технологического стека, реализацию ключевых модулей и оценку качества полученного решения на тестовых данных.")

P("Целью курсовой работы является разработка прототипа интеллектуального интерфейса, позволяющего пользователю формулировать запросы к реляционной базе данных на естественном языке (русском и английском) и получать корректные SQL-запросы с возможностью их выполнения.")

P("Для достижения поставленной цели необходимо решить следующие задачи:", )

ts = [
"Провести анализ теоретических основ задачи NL2SQL, включая существующие подходы, архитектуры нейронных сетей и методы оценки качества.",
"Выполнить обзор и сравнительный анализ современных NL2SQL-систем, а также технологических решений, используемых для их реализации.",
"Спроектировать модульную архитектуру прототипа интеллектуального интерфейса, определить функциональные требования и выбрать технологический стек.",
"Реализовать прототип, включающий модули обработки естественного языка, генерации SQL-запросов на основе LLM, валидации результатов и пользовательский интерфейс.",
"Провести тестирование разработанного прототипа на наборе тестовых запросов различной сложности и оценить точность генерации SQL.",
"Сформулировать выводы о перспективах развития и возможных направлениях дальнейшего совершенствования разработанного решения."
]

for i, t in enumerate(ts, 1):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.line_spacing = 1.5
    pf.space_after = Pt(2)
    pf.left_indent = Cm(1.25)
    pf.first_line_indent = Cm(0)
    r = p.add_run(f"{i}. {t}")
    r.font.name = "Times New Roman"
    r.font.size = Pt(14)

P("В ходе выполнения работы использовались следующие методы исследования: теоретический анализ научно-технической литературы по теме NL2SQL и смежным областям обработки естественного языка; сравнительный анализ существующих программных решений и подходов к генерации SQL-запросов; методы объектно-ориентированного проектирования и моделирования программных систем; экспериментальное тестирование разработанного прототипа на контрольном наборе запросов с последующей оценкой точности.")

P("Практическая значимость работы заключается в создании работающего прототипа интеллектуального интерфейса, который может быть использован для упрощения доступа к данным в реляционных базах для специалистов, не владеющих SQL. Результаты работы могут найти применение в образовательных целях для демонстрации возможностей современных LLM в задачах Text-to-SQL, а также послужить основой для дальнейшей разработки промышленного решения.")

P("Структура работы определена поставленными задачами и логикой исследования. Работа состоит из введения, трёх глав, заключения, списка литературы и приложений.", )

P("В первой главе рассматриваются теоретические основы генерации SQL-запросов из естественного языка: анализируется сущность проблемы NL2SQL, прослеживается эволюция подходов к её решению от правиловых систем до современных нейросетевых методов, описываются ключевые архитектуры нейронных сетей, применяемые в задаче Text-to-SQL, а также существующие бенчмарки и метрики оценки качества.", )

P("Вторая глава посвящена анализу существующих решений и технологий. В ней проводится обзор современных NL2SQL-систем, сравнительный анализ подходов на основе больших языковых моделей, рассматриваются инструменты и фреймворки для разработки, а также формулируется постановка задачи на разработку прототипа.", )

P("В третьей главе описывается процесс разработки прототипа интеллектуального интерфейса: архитектура программного решения, проектирование базы данных, реализация модулей обработки естественного языка, генерации SQL-запросов и валидации, пользовательский интерфейс, а также результаты тестирования разработанного прототипа.", )

P("В заключении подводятся итоги работы, формулируются основные результаты и намечаются перспективы дальнейшего развития. Список литературы включает ключевые научные публикации и технические источники по теме исследования.", )

